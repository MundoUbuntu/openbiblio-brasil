<h1> Compreendendo as alterações de status da bibliografia: </ h1>
A tabela a seguir lista os estados de status possíveis para uma cópia de bibliografia. <br> <br>
<table class = "primary">
  <tr> <th> Status </ th> <th> Descrição </ th> </ tr>
  <tr> <td class = "primary" valign = "top"> verificado em </ td> <td class = "primary"> A bibliografia é arquivada e disponível para checkout. </ Td> </ tr>
  <tr> <td class = "primary" valign = "top"> check out </ td> <td class = "primary"> A bibliografia é verificada por um membro da biblioteca. </ Td> </ tr>
  <tr> <td class = "primary" valign = "top"> em espera </ td> <td class = "primary"> A bibliografia está sendo realizada para retirada por um membro que tenha depositado a bibliografia. </ td> </ tr>
  <tr> <td class = "primary" valign = "top"> carrinho de estante </ td> <td class = "primary"> A bibliografia está no carrinho de prateleira aguardando ser arquivada. </ td> </ tr>
  <tr> <td class = "primary" valign = "top"> danificado / reparo </ td> <td class = "primary"> A bibliografia está sendo reparada devido a danos. </ td> </ tr>
  <tr> <td class = "primary" valign = "top"> área de exibição </ td> <td class = "primary"> A bibliografia não está disponível para checkout porque está em uma vitrine. </ td> </ tr>
  <tr> <td class = "primary" valign = "top"> lost </ td> <td class = "primary"> A bibliografia não é disponível para checkout porque não pode ser encontrada. </ td> </ tr>
  <tr> <td class = "primary" valign = "top"> em empréstimo </ td> <td class = "primary"> A bibliografia está em empréstimo. </ td> </ tr>
  <tr> <td class = "primary" valign = "top"> em ordem </ td> <td class = "primary"> A bibliografia está em ordem e ainda não chegou. </ td> </ tr>
</ table>
