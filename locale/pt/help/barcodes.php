<a name="memb"> Para códigos de barras de membros use uma estrutura de numeração com liderança
Zeros, de modo que haja um número fixo de dígitos para todos os números de códigos de barras. </a>
<br> <br> <br>

<a name="p_id"> Imprima os cartões de membro da seguinte forma: </a>
<ul>
  <li> Acesse a guia Relatórios, execute o relatório da Pesquisa de Membros. </ li>
  <li> Transforme os resultados do relatório em cartões selecionando um layout de cartão
  Submenu da navegação à esquerda. </ li>
</ ul>
<br>

<a name="copy"> Para bibliografia, os códigos de barras da estrutura de numeração podem ser: </a>
<ul>
  <li> Externo </ li>
  <li> Interno, usando o
    <a href="../shared/help.php?page=biblioCopyEdit#auto"> Autogenerate </a> opção </ li>
</ ul>
<br>

<a name="p_ba"> Imprima os rótulos dos códigos de barras de mídia da seguinte forma: </a>
<ul>
  <li> Acesse a guia Relatórios, execute o relatório de pesquisa de cópia. </ li>
  <li> Transforme os resultados do relatório em etiquetas de código de barras selecionando uma etiqueta
  Submenu de layout da navegação à esquerda. </ li>
</ ul>
<br>

<a name="scan"> Qualquer scanner de código de barras que emula o teclado é suportado
Por OpenBiblio. </a>
Isso inclui scanners USB e scanners que se conectam entre o computador
E seu teclado (cunha do teclado).
<br>
A maioria dos scanners de código de barras pode ser programado para anexar automaticamente uma carruagem
Retorno (mesmo que acertar ENTER no teclado) para todas as digitalizações. Nesse caminho,
O OpenBiblio poderá processar seu pedido assim que você digitalizar o item.